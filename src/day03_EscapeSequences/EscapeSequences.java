package day03_EscapeSequences;
/*
escape sequences:must be given with double quote
\n:start a new line

\h:paragraph space tab
\\:single back slash
\":double quote


 */
public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Java\nPython\nC");


        System.out.println("------------------------");
        System.out.println("Hello Cydeo. \nHow are you?\n It is nice to see you all!\n What class do we have next week?");

        System.out.println("\tJava is language program");

        System.out.println("--------------------------------------");

        System.out.println("/ \\");// in order to print a backward slash we need to  use two backward slash
        System.out.println("----------------------");
        System.out.println("my favourite TV show is \"Game of Throne\"");
        // in order to print double quote we need to double quote


    }
}
