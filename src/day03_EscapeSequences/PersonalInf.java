package day03_EscapeSequences;

public class PersonalInf {
    public static void main(String[] args) {
        System.out.println("Name:Fatma");
        System.out.println("Gender:Female");
        System.out.println("date of birth:15/02/1972");
        System.out.println("Address:London");
        System.out.println("Phone number:7687045");
        System.out.println("SSN:(**)-***-678");
    }

}
